     */
    public function store(Request $request)
    {
        $image_name = $request->file('image')->hashName();
        $path = $request->file('image')->store('public/images/items');

        $news = new News;
        $news->title = $request->title;
        $news->date = $request->date;
        $news->content = $request->content;
        $news->image = '/storage/images/items/'.$image_name;
        $news->save();

        return redirect()->route('news.create')->with('message','News Item Created Successfully');
    }

